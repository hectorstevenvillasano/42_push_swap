#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t s1_l;

	s1_l = ft_strlen(s1);
	i = 0;
	while (n && s2[i] != '\0')
	{
		s1[s1_l + i] = s2[i];
		n--;
		i++;
	}
	s1[s1_l + i] = '\0';
	return (s1);
}
