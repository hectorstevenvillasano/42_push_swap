#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t s1_l;
	size_t i;

	i = 0;
	s1_l = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[s1_l + i] = s2[i];
		i++;
	}
	s1[s1_l + i] = '\0';
	return (s1);
}
