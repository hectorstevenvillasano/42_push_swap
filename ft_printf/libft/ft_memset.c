#include "libft.h"

void				*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*s;
	int				i;

	s = b;
	i = 0;
	while (len--)
	{
		s[i] = c;
		if (len)
			i++;
	}
	return (b);
}
